// Data types in Dart 

void main() {
  // Strings
    String firstName = "Ahmed";
    print("String: $firstName");

  // Integers
    int myNum = 41;
    print("Integer: $myNum");

  // Doubles
    double otherNum = 19.65;
    print("Double: $otherNum");

  // Booleans
    bool myBool = false;
    print("Boolean: $myBool");

  // Dynamic
    dynamic fullName = "Ahmed Mukhtar";
    print("Dynamic: $fullName");
}